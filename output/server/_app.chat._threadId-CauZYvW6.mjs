function extractDisplayAndFiles(rawText) {
  let displayContext = rawText;
  const modePrompts = ["You are a research assistant. Provide concise factual answers and cite sources.", "You are an educator. Answer in a structured learning format: definition, features, functionality, procedure, uses, advantages.", "You are a planner. Provide a detailed step‑by‑step plan with milestones and optional timelines."];
  for (const p of modePrompts) {
    if (displayContext.startsWith(p)) {
      displayContext = displayContext.substring(p.length).trim();
    }
  }
  const files = [];
  const fileRegex = /<file name="([^"]+)">[\s\S]*?<\/file>/g;
  let match;
  while ((match = fileRegex.exec(displayContext)) !== null) {
    files.push(match[1]);
  }
  displayContext = displayContext.replace(fileRegex, "").trim();
  return {
    displayContext,
    files
  };
}
